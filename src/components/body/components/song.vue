《金曲捞》情怀首播：
每一首遗失的金曲背后，都藏着一个柔软的故事
打捞岁月金曲，唤醒音乐记忆。
4月14日周五晚，音乐原创节目《金曲捞》正式登陆江苏卫视，薛之谦、何洁、金志文三位唤醒师的登台献唱，不仅让曾经遗失在岁月里的《沉迷》、《把你藏在歌里面》、《王子公主》重见天日，更让这些老歌焕发出令人惊喜的全新光芒。
歌曲之外，更打动观众的是潜藏在那些音符之中华语乐坛黄金时代的璀璨荣光。《沉迷》承载着玉女掌门人难以启齿的情感往事，《把你藏在歌里面》又有着谭咏麟与张国荣的交情匪浅，《王子公主》则包含着乐坛大佬之间的惺惺相惜。
所有这些“乐坛记忆的集体温习”让观众感叹，“每一首遗失的金曲背后，都藏着一个柔软的故事”。
恋爱每个阶段都有她的模样
人生如歌 梁咏琪用《沉迷》致敬爱情
用摇滚精神演绎玉女转型之作，收录在梁咏琪2004年的专辑《归属感》里的《沉迷》，在金志文的全新演绎下绽放出了独具一格的全新光芒。叨叨团成员黄国伦听完改编版的《沉迷》后击节赞赏，“我大胆预测，这样的改编是非常大胆的冒险，与原唱的版本完全不一样，你为什么要改得如此Rock？”
让金志文下定决心大胆改编的，是《沉迷》里梁咏琪对爱情的表达。梁咏琪透露，《沉迷》的创作初衷是改变，“《归属感》已经是我的第九张专辑，那时想抓到耳目一新的感觉。唱的时候觉得跟我的个性很不像，太沉重了。至于崩溃大哭，也许就像演戏投入角色一样，投射到歌词里时就哭个不停。”
巧合的是，《沉迷》发行的2004年距离郑伊健和梁咏琪无奈分手的2005年时间很近。谈论起爱情，梁咏琪的回答令观众唏嘘，“每个人都有过一些关于爱情的故事，它是你身边人未必能接受，但你却沉迷其中的。《沉迷》想唱出来的，就是这种自我沉淀的感觉。”
与金志文的感受一样，不少歌迷表示梁咏琪的歌曲一向都在描摹爱情里的各种模样：“初恋时，要唱《新鲜》；热恋时，要唱《中意他》；失恋时，则是《短发》。”
追忆香港乐坛的黄金年代
回忆暗涌 《沉默是金》缅怀张国荣
“我把这首《把你藏在歌里面》送给那些在身边走走停停的人，期待人海中的你，能听到我的歌声。”
唤醒师何洁把“从歌词里读到的痛”深藏在旋律里，一下子打动了叨叨团里的“蟹黄CP”。擅长鬼马搞笑的薛之谦一脸严肃，表示“被第二句歌词‘击退’了”；黄国伦也没有与“冤家”薛之谦互怼，称“听完后很伤感”。
这首被称作“歌迷睡前必听曲目”的《把你藏在歌里面》，曾经让原唱谭咏麟哭到崩溃，“歌词是施人诚写的，我进了录音棚才看到歌词，一边唱一边哭。”对于自己的作品惜被“蒙尘”，谭咏麟满心遗憾，他说，录制时，自己一唱起《把你藏在歌里面》眼前就会浮现画面感，“甚至有时还想，如果能回到香港乐坛还有新歌打榜的时候，我能让它在榜单上多停留一个月甚至一个星期，这首歌的命运就会不同。”
最让粉丝感慨万千的，还有谭咏麟与张国荣之间的如风往事。谭咏麟说，那时很多媒体故意炒作自己与张国荣间的“水火不容”，“其实我们私底下还开玩笑，‘昨天媒体上又在说什么？’”为了缅怀“哥哥”，谭咏麟现场还唱起经典曲目《沉默是金》，让叨叨团瞬间泪目，也被观众点评为“全场最感人的催泪炸弹”。
乐坛师徒《金曲捞》再续前缘
念念不忘 张信哲调侃薛之谦曾不会讲段子
被唤醒的不仅仅是金曲，还有华语乐坛被尘封的记忆。谈到《王子公主》的创作历程，张信哲分享了曾与“大哥”李宗盛合作的逸闻趣事，“《信仰》是我很喜欢的一张专辑，那时我还住在大哥家里，他暂别歌坛两年，退隐在家专心做音乐，每天写歌创作。制作《信仰》时，我第一见到他，原来他不是那种‘文思泉涌，十分钟写一首歌’的创作人，而是字字斟酌、字字推敲的创作者，这样的创作过程太让我享受了。”
而之所以指定薛之谦为唤醒师，是因为张信哲与其相识多年，可谓渊源颇深，“那时候是2005年，薛之谦参赛，而我是评审。他的中音很好听，加上帅气的外表，绝对是当白马王子的不二人选，觉得倘若朝着这个路线发展会有很好的结果。总之，对他印象很好，就结下了渊源。”
念念不忘，必有回响。张信哲说，由此自己跟薛之谦结为好友，所以这次唤醒《王子公主》他第一时间就想到了薛之谦。
张信哲和薛之谦到底有多熟？《金曲捞》现场也见证了爆笑一幕——面对薛之谦“他是我偶像”的“表白”时，张信哲笑言“你这么红，我要蹭一下你的热度”，并调侃徒弟终于没有那么羞涩了，“当时参赛时薛之谦还很羞涩，在后台碰到我都低头不敢讲话，也不懂讲段子。”
